def build_branded_html_document(
    memo_markdown: str,
    company_name: str,
    generated_at: str,
) -> str:
    """
    Wrap the memo's converted HTML body in a complete, branded HTML
    document with AIRP-themed CSS, ready for WeasyPrint to render.
    The page-numbering and company-name header come from CSS @page
    rules (rendered by WeasyPrint, not by this function directly) so
    they appear identically on every page without per-page logic here.
    """
    body_html = markdown_to_html(memo_markdown)
    css = _CSS_TEMPLATE.format(
        brand_color=_AIRP_BRAND_COLOR,
        brand_color_dark=_AIRP_BRAND_COLOR_DARK,
        company_name=html.escape(company_name),
        generated_at=html.escape(generated_at),
    )
    return (
        "<!DOCTYPE html>\n"
        '<html>\n<head>\n<meta charset="utf-8">\n'
        f"<style>{css}</style>\n</head>\n<body>\n"
        f"{body_html}\n</body>\n</html>"
    )


# ---------------------------------------------------------------------------
# PDF rendering + atomic write
# ---------------------------------------------------------------------------


def render_memo_pdf(
    memo_markdown: str,
    company_name: str,
    generated_at: str,
    job_id: str,
) -> Optional[Path]:
    """
    Render memo_markdown to a branded PDF and write it to disk.

    Returns the absolute Path written on success, or None if PDF
    generation is disabled, WeasyPrint is unavailable, or any step
    fails. Never raises -- every failure is logged and degrades to
    None so the caller can proceed with the Markdown memo alone.
    """
    pdf_enabled = (
        getattr(settings, "feature_pdf_enabled", True) if settings is not None else True
    )
    if not pdf_enabled:
        logger.info("render_memo_pdf: feature_pdf_enabled=False -- skipping")
        return None

    try:
        from weasyprint import HTML  # noqa: PLC0415 -- lazy, optional system dep
    except (ImportError, OSError) as exc:
        # ImportError: the weasyprint package itself is not installed.
        # OSError: the package IS installed but a native dependency
        # (Pango/Cairo/GDK-Pixbuf, surfaced via cffi's dlopen) could not
        # be loaded -- e.g. installed without its system libraries, or
        # a broken/partial install. Both cases mean PDF export cannot
        # proceed; both must degrade gracefully rather than crash the
        # pipeline.
        logger.warning(
            "render_memo_pdf: WeasyPrint not available -- skipping PDF export "
            "(install weasyprint and its system dependencies: Pango, Cairo, "
            "GDK-Pixbuf): %s",
            exc,
        )
        return None

    try:
        html_document = build_branded_html_document(
            memo_markdown=memo_markdown,
            company_name=company_name,
            generated_at=generated_at,
        )
        pdf_bytes: bytes = HTML(string=html_document).write_pdf()
    except Exception as exc:  # noqa: BLE001 -- this step must never raise
        logger.warning("render_memo_pdf: PDF rendering failed: %s", exc)
        return None

    target = resolve_memo_pdf_path(job_id)

    try:
        target.parent.mkdir(parents=True, exist_ok=True)
        # Atomic write: temp file in the same directory as the target,
        # then os.replace() -- atomic on POSIX, best-effort on Windows.
        # Same pattern as backend/graph/graph_visualisation.py.
        tmp_fd, tmp_path_str = tempfile.mkstemp(
            dir=target.parent,
            prefix=".memo_pdf_tmp_",
            suffix=".pdf",
        )
        try:
            with os.fdopen(tmp_fd, "wb") as tmp_file:
                tmp_file.write(pdf_bytes)
            os.replace(tmp_path_str, str(target))
        except Exception:
            try:
                os.unlink(tmp_path_str)
            except OSError:
                pass
            raise
    except Exception as exc:  # noqa: BLE001 -- this step must never raise
        logger.warning("render_memo_pdf: writing PDF to disk failed: %s", exc)
        return None

    logger.info(
        "render_memo_pdf: wrote %s (%d bytes) for job_id=%s",
        target,
        len(pdf_bytes),
        job_id,
    )
    return target


# ---------------------------------------------------------------------------
# LangGraph node entry point
# ---------------------------------------------------------------------------


def pdf_export_node(state: dict[str, Any]) -> dict[str, Any]:
    """
    LangGraph node entry point (T-043). Reads state["memo_markdown"]
    (written by report_generator_node in T-042) and renders it to a
    branded PDF on disk. Returns {"memo_pdf_path": str | None}.

    Never raises. A missing memo_markdown, a disabled feature flag, a
    missing WeasyPrint installation, or any rendering/write failure all
    degrade to memo_pdf_path=None -- the Markdown memo from T-042
    remains available regardless of PDF export's outcome.
    """
    from datetime import datetime  # noqa: PLC0415 -- avoids module-level import

    # for a stdlib symbol only needed in this one function; keeps the
    # module's import block focused on this module's actual external deps.

    job_id = state.get("job_id", "unknown")
    company_name = state.get("company_name", "Unknown Company")
    memo_markdown = state.get("memo_markdown")

    if not memo_markdown:
        logger.warning(
            "pdf_export_node: no memo_markdown in state for job_id=%s -- "
            "skipping PDF export",
            job_id,
        )
        return {"memo_pdf_path": None}

    generated_at = datetime.utcnow().strftime("%d %b %Y, %H:%M UTC")

    pdf_path = render_memo_pdf(
        memo_markdown=memo_markdown,
        company_name=company_name,
        generated_at=generated_at,
        job_id=job_id,
    )

    return {"memo_pdf_path": str(pdf_path) if pdf_path is not None else None}


